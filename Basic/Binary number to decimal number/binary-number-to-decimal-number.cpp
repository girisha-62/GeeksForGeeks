class Solution {
  public:
    int binary_to_decimal(string str) {
        // Code here.
        int n=str.length();
        int d,val=0;
        for(int i=0;i<n;i++){
            d=str[i]-'0';
            val=val+pow(2,n-1-i)*d;
        }
        return val;
    }
};

